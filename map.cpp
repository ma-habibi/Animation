/* Init map buffer. */
Map::Map(SDL_Renderer *renderer) {
  // Load Assets
  dust = TextureManager::load_texture(
      renderer, "/Users/mahdi/Code/cpp/Graphics/Animation/assets/Dust.png");
  bush = TextureManager::load_texture(
      renderer, "/Users/mahdi/Code/cpp/Graphics/Animation/assets/Bush.png");
}

Map::~Map() {}

/* Render map */
void Map::draw(SDL_Renderer *renderer) {
  // 800 600 each rect 25, 25
  for (int i = 0; i < 24; i++) {
    for (int j = 0; j < 32; j++) {
      // Set pos and size of texture
      SDL_Rect dst_rect = {j * 25, i * 25, 25, 25};

      switch (layout[i][j]) {
      case 0: // Draw bush
        SDL_RenderCopy(renderer, bush, NULL, &dst_rect);
        break;
      case 1: // Draw dust
        SDL_RenderCopy(renderer, dust, NULL, &dst_rect);
        break;
      default:
        throw std::runtime_error("Map Failed to init.");
      }
    }
  }
}